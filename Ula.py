from Memoria import *

class Ula(object):

    def soma(self, Reg1, Reg2):
        Reg1 = Reg1 + Reg2
        return Reg1 

    def subtracao(self, Reg1, Reg2):
        Reg1 = Reg1 - Reg2
        return Reg1

    def multiplicacao(self, Reg1, Reg2):
        Reg1 = Reg1 * Reg2
        return Reg1

    def and_logico(self, Reg1, Reg2):
        Reg1 = Reg1 & Reg2
        return Reg1 

    def or_logico(self, Reg1, Reg2):
        Reg1 = Reg1 | Reg2
        return Reg1


